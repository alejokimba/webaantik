function MostrarModalPagoProy(){
    
    const modalpagoproyfin=document.getElementById('paypal-proyfin-modal');
    modalpagoproyfin.style.display='block';
   $(function(){
   
	$("#btn-cerrar-pago").on("click", function(){
        
       $('#paypal-proyfin-modal').fadeOut();
          });
		  


   });
    
};
function cerrarModalPagoProy(){
	const modalpagoproyfin=document.getElementById('paypal-proyfin-modal');
    modalpagoproyfin.style.display='none';
};
